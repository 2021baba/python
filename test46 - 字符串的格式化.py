#按一定格式输出的字符串
'''1.%作为占位符'''
# %s字符串
# %i,%d整数
# %f浮点数
name = '张三'
age = 20
print('我叫%s,今年%i岁' % (name, age))

'''{}作为占位符'''
print('我叫{0},今年{1}岁'.format(name, age))

'''f-string'''
print(f'我叫{name},今年{age}岁')

print('%10d' % 99)#%10d指宽度
print('%.3f' % 3.1415926)#%.3f保留3位小数
print('%10.3f' % 3.1415926)#'%10.3f'同时表示宽度和精度

print('{0:.3}'.format(3.1415926))#:.3表示一共3位#0表示占位符的顺序
print('{0:10.3}'.format(3.1415926))#10表示宽度
